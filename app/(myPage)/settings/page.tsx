"use client"
import { backIcon } from '@/constants/icons';
import { toast } from '@/hooks/use-toast';
import { useRouter } from 'next/navigation';
import React, { useEffect, useState } from 'react'

import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogFooter,
  DialogHeader,
  DialogTitle,
  DialogTrigger,
} from "@/components/ui/dialog"
import { useTranslations } from 'next-intl';

function Settings() {
  const router = useRouter();
  const [langPopup, setLangPopup] = useState(false);
  const t = useTranslations("MyPage.settings");

  const [locale, setLocale] = useState<string>("");

  useEffect(()=>{
    const cookieLocale = document.cookie.split("; ").find((row)=>row.startsWith("TORITORA_LOCALE="))?.split("=")[1];
    if(cookieLocale){
      setLocale(cookieLocale);
    }
    else {
      const browserLocale = navigator.language.slice(0,2);
      setLocale(browserLocale);
      document.cookie = `TORITORA_LOCALE=${browserLocale};  path=/; max-age=31536000`;
      router.refresh();
    }
  },[router])

  const changeLocale = (newLocale: string) => {
    setLangPopup(false);
    setLocale(newLocale);
    document.cookie = `TORITORA_LOCALE=${newLocale};  path=/; max-age=31536000`;
    router.refresh();
  }

  const logout = async() => {
    const res = await fetch(`${process.env.NEXT_PUBLIC_BACKEND_URL}/auth/logout`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
    });

    if(res.status === 200){
      localStorage.removeItem("userProfession");
      router.replace('/onboard')
    }
    else{
      toast({
        title:"Unauthorized request",
        description:"Invalid token is found logging out",
        variant:"destructive"
      })
      router.replace('/onboard')
    }
  }

  const handleGoBack = ()=>{
    router.back();
  }

  const handleGoToLink = (route:string)=>{
    router.push(route)
  }

  const accountSettings = [
    {
      name:t("changeUserInformation"),
      link:"/editProfile",
      icon:profileIcon
    },
    {
      name:t("shootingSettings"),
      link:"/settings/shooting-settings",
      icon:locationIcon
    },
    {
      name:t("notificationSettings"),
      link:"/settings/notification-settings",
      icon:bellIcon
    },
  ]
  
  const otherSettings = [
    {
      name:t("aboutToritora"),
      link:"/settings/about-us",
      icon:infoIcon
    },
    {
      name:t("termsOfService"),
      link:"/terms-of-use",
      icon:fileIcon
    },
    {
      name:t("privacyPolicy"),
      link:"/help/privacy",
      icon:shieldIcon
    },
    {
      name:t("help"),
      link:"/help",
      icon:questionMarkIcon
    },
    {
      name:t("giveFeedback"),
      link:"/settings/feedback",
      icon:feedbackIcon
    },
  ]

  return (
    <div className='bg-[#f8fcfd] h-full'>
      <header className="sticky top-0 w-full h-[72px] flex items-center justify-center bg-white shadow-lg">
        <button onClick={handleGoBack} className='absolute top-[50%] translate-y-[-50%] left-4'>{backIcon}</button>
        <span className="text-[16px] leading-[24px] text-center font-semibold">{t("mySettings")}</span>
      </header>

      <div className='md:grid md:grid-cols-2 md:max-w-[800px] mx-auto'>
        <div className='space-y-4 mx-4 mt-8'>
          <span className='font-medium text-[12px] leading-[18px] text-[#999999]'>{t("account")}</span>
          <div className='flex flex-col items-center justify-center gap-6 p-8 bg-white rounded-lg shadow-[0_4px_20px_rgba(0,0,0,0.15)]'>
            {accountSettings.map((item,index)=>{
              return(
                <button className='w-full h-8 flex items-center justify-between' key={index} onClick={()=>handleGoToLink(item.link)}>
                  <span className='flex items-center justify-center gap-4 font-normal text-[12px] leading-[18px] text-[#111111]'>{item.icon} {item.name}</span>

                  {rightArrowIcon}
                </button>
              )
            })}

            <Dialog open={langPopup} onOpenChange={setLangPopup}>
              <DialogTrigger asChild>
                <button className='w-full h-8 flex items-center justify-between'>
                  <span className='flex items-center justify-center gap-4 font-normal text-[12px] leading-[18px] text-[#111111]'>{langIcon} {t("changeLanguage")}</span>

                  {rightArrowIcon}
                </button>
              </DialogTrigger>
              <DialogContent className="sm:max-w-[425px] rounded-md">
                <DialogHeader>
                  <DialogTitle>{t("chooseLanguage")}</DialogTitle>
                  <DialogDescription>

                  </DialogDescription>
                </DialogHeader>
                <div className="flex items-center justify-center gap-8">
                  <button onClick={()=>{changeLocale("en")}} className={`${locale === "en" ? "text-white bg-secondary" : ""} px-2 py-2 border rounded-md font-semibold text-[16px]`}>EN</button>
                  <button onClick={()=>{changeLocale("jn")}} className={`${locale === "jn" ? "text-white bg-secondary" : ""} px-2 py-2 border rounded-md font-semibold text-[16px]`}>JN</button>
                </div>
                <DialogFooter>
                </DialogFooter>
              </DialogContent>
            </Dialog>
          </div>
        </div>

        <div className='space-y-4 mx-4 mt-8'>
          <span className='font-medium text-[12px] leading-[18px] text-[#999999]'>{t("other")}</span>
          <div className='flex flex-col items-center justify-center gap-6 p-8 bg-white rounded-lg shadow-[0_4px_20px_rgba(0,0,0,0.15)]'>
            {otherSettings.map((item,index)=>{
              return(
                <button className='w-full h-8 flex items-center justify-between' key={index} onClick={()=>handleGoToLink(item.link)}>
                  <span className='flex items-center justify-center gap-4 font-normal text-[12px] leading-[18px] text-[#111111]'>{item.icon} {item.name}</span>

                  {rightArrowIcon}
                </button>
              )
            })}
            <button className='w-full h-8 flex items-center justify-between' onClick={logout}>
              <span className='flex items-center justify-center gap-4 font-normal text-[12px] leading-[18px] text-[#111111]'>{logoutIcon} {t("logout")}</span>

              {rightArrowIcon}
            </button>
          </div>
        </div>
      </div>
    </div>
  )
}

export default Settings

